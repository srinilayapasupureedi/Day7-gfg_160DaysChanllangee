class Solution {
  public:
    int maximumProfit(vector<int> &prices) {
        // code here
        int cp=prices[0];
        int maxprofit=0;
        for(int i=1;i<prices.size();i++)
        {
            //curr prices are sellin prices
            if(prices[i]>cp)
            {
                maxprofit+=prices[i]-cp;
                
            }
                
             cp=prices[i];
            
        }
        return maxprofit;
    }
};


